public class Orc {
    public void collectFood() {
        System.out.println("Orc is collecting berries");
    }

    public void attack() {
        System.out.println("Orc attempts to attack nearest player");
    }

    public void build() {
        System.out.println("Orc is digging underground tunnel");
    }
}